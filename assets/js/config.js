function mergeMap (maps) {
    let mergedMap;
    for (const mapRow of maps) {
        let mergedMapRow;
        for (const map of mapRow) {
            if (mergedMapRow) {
                for (let i = 0; i < mergedMapRow.length; i++) {
                    mergedMapRow[i] = mergedMapRow[i].concat(map[i]);
                }
            } else {
                mergedMapRow = map;
            }
        }
        if (mergedMap) {
            mergedMap = mergedMap.concat(mergedMapRow);
        } else {
            mergedMap = mergedMapRow;
        }
    }
    return mergedMap;
}

